fn main() {
    const CURRENT_YEAR: i32 = 1993;
    let ten_years_ago = CURRENT_YEAR - 10;
    let five_years_ago = CURRENT_YEAR - 5;
    let one_year_ago = CURRENT_YEAR - 1;

    println!("Ten years ago: {}", ten_years_ago);
    println!("Five years ago: {}", five_years_ago);
    println!("One year ago: {}", one_year_ago);
}